/**
 * 자유 문자열 → 알려진 렌즈만 통과. 옛 클라이언트/이상값/누락은 "default"(전방위) 로 폴백한다
 * (designer persona 의 화이트리스트 패턴과 동형 — 자유 문자열을 그대로 믿지 않는다).
 */
export function parseLens(value: unknown): PoLens {
  return value === "design" ||
    value === "bug" ||
    value === "qa" ||
    value === "security" ||
    value === "pm" ||
    value === "marketing" ||
    value === "analytics" ||
    value === "ops" ||
    value === "logic" ||
    value === "ux"
    ? value
    : "default";
}

/**
 * 전문가 «페르소나» — 프롬프트 첫 «정체성 문장» 한 줄. 렌즈가 그동안 «무엇을 우선 볼지»(머리말)만
 * 바꿨다면, 이 함수는 «누가 쓰는가»(정체성)까지 바꿔 — 고른 전문가가 PO 가 아니라 «직접» 브리프를
 * 쓰게 한다. 전엔 어느 렌즈를 골라도 프롬프트 첫 줄이 PO 로 고정돼 모든 브리프가 «PO 기반» 이었다.
 * 미션 문장·"코드 수정 금지"·intro 등 나머지 첫 문단은 각 프롬프트가 그대로 유지하고 이 함수는
 * «정체성 문장만» 돌려준다 — "default" 는 기존 PO 문장과 byte-identical 이라 전방위/옛 클라이언트
 * 회귀 0. 명칭은 researchLensHeadmatter 의 «조사 관점 — X 전문가» 와 일치시켜 한 전문가 개념을
 * 공유한다 (중복 정의 금지). collect/research 공통 SSOT.
 */
export function lensPersona(lens: PoLens): string {
  switch (lens) {
    case "design":
      return "너는 이 저장소의 «디자인 전문가» 다.";
    case "bug":
      return "너는 이 저장소의 «디버깅·신뢰성 전문가» 다.";
    case "qa":
      return "너는 이 저장소의 «QA(품질 보증) 전문가» 다.";
    case "security":
      return "너는 이 저장소의 «보안 전문가» 다.";
    case "pm":
      return "너는 이 저장소의 «기획(PM/제품) 전문가» 다.";
    case "marketing":
      return "너는 이 저장소의 «마케팅 전문가» 다.";
    case "analytics":
      return "너는 이 저장소의 «분석(analytics) 전문가» 다.";
    case "ops":
      return "너는 이 저장소의 «운영(ops) 전문가» 다.";
    case "logic":
      return "너는 이 저장소의 «로직(도메인·정합성) 전문가» 다.";
    case "ux":
      return "너는 이 저장소의 «UX(사용성) 전문가» 다.";
    default:
      // "default"(전방위) — 기존 PO 정체성과 byte-identical (회귀 0).
      return "너는 이 저장소의 프로덕트 오너(PO) 에이전트다.";
  }
}

